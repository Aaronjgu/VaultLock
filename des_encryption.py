def hex_to_bin(hex_str):
    hex_map = {
        '0': '0000', '1': '0001', '2': '0010', '3': '0011',
        '4': '0100', '5': '0101', '6': '0110', '7': '0111',
        '8': '1000', '9': '1001', 'A': '1010', 'B': '1011',
        'C': '1100', 'D': '1101', 'E': '1110', 'F': '1111'
    }
    return "".join(hex_map[digit] for digit in hex_str.upper())

def bin_to_hex(bin_str):
    hex_str = ""
    for i in range(0, len(bin_str), 4):
        chunk = bin_str[i:i+4]
        hex_str += hex(int(chunk, 2))[2:].upper()
    return hex_str

def permute(bits, table):
    return "".join(bits[position - 1] for position in table)

def left_shift(bits, n):
    return bits[n:] + bits[:n]

def xor(bits1, bits2):
    return "".join("0" if b1 == b2 else "1" for b1, b2 in zip(bits1, bits2))

def s_box_lookup(six_bits, s_box_table):
    row = int(six_bits[0] + six_bits[5], 2)
    col = int(six_bits[1:5], 2)
    val = s_box_table[row][col]
    return bin(val)[2:].zfill(4)


def generate_subkeys(k_bits):
    """Generates the 16 subkeys used for DES rounds."""
    permuted_key = permute(k_bits, PC1)
    current_c = permuted_key[:28]
    current_d = permuted_key[28:]
    shift_schedule = [1, 1, 2, 2, 2, 2, 2, 2, 1, 2, 2, 2, 2, 2, 2, 1]
    
    subkeys = []
    for shift_amount in shift_schedule:
        current_c = left_shift(current_c, shift_amount)
        current_d = left_shift(current_d, shift_amount)
        subkeys.append(permute(current_c + current_d, PC2))
    return subkeys

def process_block(block_bits, subkeys):
    """Processes a single 64-bit block through the 16 Feistel rounds."""
    permuted_m = permute(block_bits, IP)
    current_l = permuted_m[:32]
    current_r = permuted_m[32:]

    for i in range(16):
        expanded_r = permute(current_r, E)
        xor_res = xor(expanded_r, subkeys[i])
        s_output = ""
        for j in range(8):
            chunk = xor_res[j*6 : (j+1)*6]
            s_output += s_box_lookup(chunk, S_BOXES[j])
        f_res = permute(s_output, P)
        next_l = current_r
        next_r = xor(current_l, f_res)
        current_l = next_l
        current_r = next_r

    combined_final = current_r + current_l 
    return permute(combined_final, IP_INV)


def encrypt_password(plaintext_string, key_hex):
    """
    Encrypts a plain text password using DES.
    Pads the string using PKCS#7 to support variable lengths.
    """
    data = plaintext_string.encode('utf-8')
    pad_len = 8 - (len(data) % 8)
    padded_data = data + bytes([pad_len] * pad_len)
    padded_hex = padded_data.hex().upper()

    k_bits = hex_to_bin(key_hex)
    subkeys = generate_subkeys(k_bits)

    ciphertext_hex = ""
    for i in range(0, len(padded_hex), 16):
        block_hex = padded_hex[i:i+16]
        block_bits = hex_to_bin(block_hex)
        cipher_bits = process_block(block_bits, subkeys)
        ciphertext_hex += bin_to_hex(cipher_bits)

    return ciphertext_hex


def decrypt_password(ciphertext_hex, key_hex):
    """
    Decrypts a DES-encrypted hex string back to a plain text password.
    Removes PKCS#7 padding automatically.
    """
    k_bits = hex_to_bin(key_hex)
    subkeys = generate_subkeys(k_bits)
    subkeys.reverse() 

    decrypted_hex = ""
    for i in range(0, len(ciphertext_hex), 16):
        block_hex = ciphertext_hex[i:i+16]
        block_bits = hex_to_bin(block_hex)
        plain_bits = process_block(block_bits, subkeys)
        decrypted_hex += bin_to_hex(plain_bits)

    decrypted_data = bytes.fromhex(decrypted_hex)
    pad_len = decrypted_data[-1]
    unpadded_data = decrypted_data[:-pad_len]

    return unpadded_data.decode('utf-8')


