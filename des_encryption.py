def hex_to_bin(hex_str):
    hex_map = {
        '0': '0000', '1': '0001', '2': '0010', '3': '0011',
        '4': '0100', '5': '0101', '6': '0110', '7': '0111',
        '8': '1000', '9': '1001', 'A': '1010', 'B': '1011',
        'C': '1100', 'D': '1101', 'E': '1110', 'F': '1111'
    }
    binary_str = ""
    for digit in hex_str.upper():
        binary_str += hex_map[digit]
    return binary_str

def permute(bits, table):
    new_bits = ""
    for position in table:
        new_bits += bits[position - 1]
    return new_bits

def left_shift(bits, n):
    return bits[n:] + bits[:n]

def xor(bits1, bits2):
    result = ""
    for i in range(len(bits1)):
        if bits1[i] == bits2[i]:
            result += "0"
        else:
            result += "1"
    return result

def s_box_lookup(six_bits, s_box_table):
    row = int(six_bits[0] + six_bits[5], 2)
    col = int(six_bits[1:5], 2)
    val = s_box_table[row][col]
    return bin(val)[2:].zfill(4)

def bin_to_hex(bin_str):
    hex_str = ""
    for i in range(0, 64, 4):
        chunk = bin_str[i:i+4]
        hex_digit = hex(int(chunk, 2))[2:].upper()
        hex_str += hex_digit
    return hex_str

